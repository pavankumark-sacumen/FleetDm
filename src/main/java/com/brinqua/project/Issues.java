package com.brinqua.project;

public class Issues {
	private int total_issues_count;
	private int failing_policies_count;

	public int getTotal_issues_count() {
		return total_issues_count;
	}

	public void setTotal_issues_count(int total_issues_count) {
		this.total_issues_count = total_issues_count;
	}

	public int getFailing_policies_count() {
		return failing_policies_count;
	}

	public void setFailing_policies_count(int failing_policies_count) {
		this.failing_policies_count = failing_policies_count;
	}

}
